#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t j;
	size_t i;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	i--;
	while (j < n)
	{
		if (s2[j] == '\0')
			break ;
		i++;
		s1[i] = s2[j];
		j++;
	}
	i = i + 1;
	s1[i] = '\0';
	return (s1);
}
