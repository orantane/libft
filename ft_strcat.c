#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int j;
	int i;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[j] != '\0')
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
