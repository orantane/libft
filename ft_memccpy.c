#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	unsigned char	d;
	size_t			i;

	i = 0;
	d = (unsigned char)c;
	a = (unsigned char *)dst;
	b = (unsigned char *)src;
	while (i < n)
	{
		a[i] = b[i];
		if (b[i] == d)
			return (dst + i + 1);
		i++;
	}
	return (0);
}
